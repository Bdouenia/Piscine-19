#include <stdio.h>
#include <string.h>

int		ft_strcmp(char *s1, char *s2)
{
	int a;

	a = 0;
	while ((s1[a] == s2[a]) && (s1[a]) && (s2[a]))
		a++;
	return ((char)s1[a] - (char)s2[a]);
}
